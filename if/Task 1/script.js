const dayWeek = prompt(`Enter a number day of the week`);

switch (dayWeek) {
	case "1":
		console.log("Monday")
		break
	case "2":
		console.log(`Tuesday`)
		break
	case "3":
		console.log(`Wednesday`)
		break
	case "4":
		console.log(`Thursday`)
		break
	case "5":
		console.log(`Friday`)
		break
	case "6":
		console.log(`Saturday`)
		break
	case "7":
		console.log(`Sanday`)
		break
	default:
		console.log(`Enter unvalid number of the week`)

}





